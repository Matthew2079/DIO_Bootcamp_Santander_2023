# Código Resolução Vscode

saldo_atual = float(input("Digite seu saldo em R$: "))
valor_deposito = float(input('Digite o valor do depósito R$: '))
valor_retirada = float(input("Digite o valor de saque em R$: "))

#TODO: Calcular o saldo atualizado de acordo com a descrição deste desafio.
resultado = saldo_atual + valor_deposito - valor_retirada

#TODO: Imprimir o a saída de conforme a tabela de exemplos (uma casa decimal).
print(f'Saldo atualizado na conta: {resultado:.1f}')




# Variavéis para comparação
saldo = 200
saque = 200

print(saldo == saque) # sinal de igualdade
print(saldo != saque) # sinal de diferença
print(saldo > saque) # sinal maior que
print(saldo >= saque) # sinal maior ou igual que
print(saldo < saque) # sinal menor que
print(saldo <= saque) # sinal menor que ou igual
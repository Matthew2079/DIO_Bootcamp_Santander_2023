
# Sinal de atribuição
saldo = 200
print(saldo)

# Sinal de atribuição com adição
saldo += 10
print(saldo)

# Sinal de atribuição com subtração
saldo -= 20
print(saldo)

# Sinal de atribuição com divisão
saldo /= 2
print(saldo)

# Sinal de atribuição com multiplicação
saldo *= 2
print(saldo)

# Sinal de atribuição com divisão inteira
saldo //= 2
print(saldo)

# Sinal de atribuição com exponenciação
saldo **= 2
print(saldo)

# Sinal de atribuição com módulo
saldo %= 3
print(saldo)
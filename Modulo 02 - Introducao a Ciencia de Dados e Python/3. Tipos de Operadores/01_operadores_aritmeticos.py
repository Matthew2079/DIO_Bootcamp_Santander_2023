
# Variavéis para cálculo
produto_1 = 10
produto_2 = 20

print(produto_1 + produto_2) # somando valores
print(produto_1 - produto_2) # subtraindo valores
print(produto_1 / produto_2) # dividindo valores com resto
print(produto_1 // produto_2) # dividindo valores apenas parte inteira
print(produto_1 * produto_2) # multiplicando valores
print(produto_1 % produto_2) # obtendo módulo
print(produto_1 ** produto_2) # exponenciação de valores

# Validando precedência dos operadores
x = 10 + 5 * 4
y = 10 / 2 + 25 * 2 - 2 ** 2
print(x)
print(y)